        <h2>Isvägen – vintervägen</h2>
        <p>
        Under tusentals år och ända in på 1900-talet var det lättare att färdas på land under vintern än på sommaren, särskilt för långa och tunga transporter.<br><br>

        Då kunde man lämna vägen längs de backiga åsryggarna och knaggliga skogshöjderna för att söka sig ner till Sveriges flacka vägnät: frusna havsvikar, sjöar, åar och sankmarker. Dessutom förkortades ofta transportsträckan avsevärt.<br><br>

        Under kalla vintrar hade Nättraby vinterväg över isen in till Karlskrona. Första generationen isväg började strax öster om Nättrabyåns åmynning, på Wrängö. Landvägen dit gick uppe på bergsryggen öster om ån, väster om nuvarande Sjuhallavägen.<br><br>

        Men eftersom isen var vansklig bredvid en åmynning bestämdes det runt 1820, i samband med skifte, att isvägen skulle börja en bit ytterligare österut, vid Sjuhalla, precis nordväst om nuvarande Sjuhalla badplats.<br><br>

        Vägen på isen gick sedan sydsydost och över Ramsö. Skälet var att det är strömt mellan Ramsö och Sjuhallafastlandet och att isen nästan aldrig lägger sig där. Än idag syns på Ramsö de på högkant ställda naturstenarna på båda sidor som markerade vägen vintertid.<br><br>

        Isvägen fortsatte sydöst över Danmarksfjärden mot Dragsö, där den gick över det smala och flacka näset Dragsö ed där Dragsö badplats nu finns (tidigare ”Killingaviken”). Isvägen gick över Borgmästarefjärden och vid västra sidan av Fiskbrokajen finns fortfarande uppfarten till Trossö och Karlskrona centrum.<br><br>

        En av anledningarna till att isvägen skapades var att Trossö (nuvarande Karlskronas centrumö), Saltö och Dragsö tillhörde Nättraby församling. Trossös ägare Vittus Andersson med familj och personal måste ta sig till Nättraby kyrka varje söndag på grund av kyrkoplikten. Detta var innan Karlskrona grundades 1680 och fick egen församling.<br><br>

        Senare var isvägen en viktigt transportled, exempelvis fraktades ved, mellan Nättraby med omgivande fastland och anlagda staden Karlskrona.<br><br>

        För att nå den nya isvägen från 1820 byggdes strax öster om bergsryggen öster om ån och en ny spikrak väg, kantad av stenmur i norr och trädrad i söder, vidare sydösterut till Sjuhalla, som är nuvarande Sjuhallavägen.<br><br>

        Vid Hjalmarsberg svängde vägen höger över Hjalmarsbergs gård och rakt söderut mot havet, vid den då oexploaterade Sjuhallakusten. Första delen av Sjuhallavägen har alltså tillkommit på grund av isvägen.<br><br>

        Sträckan Nättraby-Karlskrona mer än halverades via isvägen jämfört med landvägen! Den plogades och markerades med tallkvisar och enbuskar av bönderna, och har aldrig varit en av Vägverkets officiella isvägar.<br><br>

        I Karlskrona skärgård fanns lokala islotsar, ofta extraknäckande småbrukare. De kontrollerade isen, stakade med hjälp av enruskor ut isvägar (trader) och kunde även hjälpa folk över isen under dåligt väder och svåra förhållanden. En särskild isyxa med avlångt, närmast spetsigt, yxhuvud användes för att fresta isen, det vill säga hugga hål i isen och bedöma om den höll eller inte. Islotsarna fick med tiden stor kunskap om isen och dess bärighet, hur den förändras med väderleken och framför allt hur man uppträder på isen. Jobbet gick ofta från far till son. Ibland kunde befintlig is förstärkas genom att vattenbegjutas, vilket frös till tjockare is. Isvägarna gick inte bara mellan Karlskrona och skärgården utan också mellan de bebodda öarna som förr saknade broförbindelse. Först tilläts gående och cyklande på isvägen, när isen blivit tillräckligt tjock även bilar och lastbilar.<br><br>

        Än idag kan isväg dras vid mycket kalla vintrar mellan Karlskrona och Aspö, som normalt har Vägverkets färjeförbindelse. Senast detta skedde var vintern 1986-1987. Isvägen börjar då vid Saltö strand och plogades med traktorer och markerades med enruskor av söner till den tidigare islotsen. Isvägar kan alltså vara en realitet än idag även i södra Sverige.<br><br>

        Medeltidens kungar red alltid Eriksgatan på vintern, marknader och högtider hölls företrädesvis under kalla årstiden. En del samhällen i inlandet var helt beroende av vinteris för att överleva.<br><br>

        Med släde och häst färdades man häpnadsväckande mjukt och tyst över vit snö och flack is, samtidigt som avstånden kortades. Vandrarna använde slipade djurben som skridskor för isen (isläggar), och utnyttjade snöskor och skidor på snön.<br><br>

        Karl X Gustavs armé kunde exempelvis 1658 ta sig isvägen över Stora bält och förändrade historien: Danmark tvingades lämna över Skåne, Halland och Blekinge till Sverige.<br><br>

        Under andra världskrigets kalla vintrar gick det isväg över Kvarken i norra Östersjön.<br><br>

        Än idag märker Vägverket ut ett tiotal isvägar, huvudsakligen i norra Sverige, när vintern blivit för kall för vägfärjorna. Vägverket går ut och plogar isvägen när isen blivit 20 cm tjock. När isen är snöfri ökar den snabbt i tjocklek och bärighet. Det måste vara minst 25 cm kärnis innan Vägverket öppnar för trafik.<br><br>

        Maxhastigheten på isvägar är 30 km/h. Minst 50 meters avstånd ska hållas mellan varje fordon. Det råder stopp- och parkeringsförbud på isväg. Skyltar kan finnas om maximal fordonsvikt.<br><br>

        Förr var vinterns isvägar ett unikt snabbt och komfortabelt färdsätt för norra Europa. Men numera ses is och snö tvärtom som bekymmer för trafiken.<br><br>

        Unika vinterfordon som utvecklats är släden dragen av hundspann, specialbyggd båt med järnskodd köl och medar, sparkstöttingen och snöskotern.<br><br>

