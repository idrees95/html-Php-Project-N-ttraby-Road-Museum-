        <h2>E22-motorvägen</h2>
        <p>
            En motorväg består av två skilda körbanor med minst två filer i vardera riktningen och med mittremsa eller mitträcke emellan. Motorvägar har också planskilda korsningar och särskilda på- och avfarter. Motorvägarna är de snabbaste och säkraste vägarna med en högsta tillåten hastighet i Sverige på 120 km/h, från hösten 2008 då de nya hastighetsgränserna 40, 60, 80, 100 och 120 införes.<br><br>

            Tvåfiliga och 13 meter breda riksväg 15 byggdes 1993 om till motorväg från Nättraby och 3 km österut till Rosenholm, Karlskronas västra avfart, som ett första steg till förbifart Karlskrona.<br><br>

            E 22 går nu där riksväg 4 byggdes 1961, något söder om gamla landsvägen. När Sverige fick Europavägar 1962 klassades riksväg 4 ner till riksväg 15.<br><br>

            Den fyrfiliga asfalterade motorvägen med vägren är totalt 24,5 meter bred med en mittbarriär av betongblock, som är fästade vid varandra men inte i vägbanan. Betongbarriärer används inte längre eftersom det anses att framhjulsdrivna bilar kan klättra över barriären.<br><br>

            Åt öster finns bullerplank, eftersom ljudet är påtagligt från den snabba motorvägen. Längre bort sidoräcken eller mjuka slänter och viltstängsel. Åt väster ligger avfarterna och viadukten (bro över väg) till Nättraby, plus busshållplatser, samtidigt som motorvägen övergår i vanlig tvåfilig väg med 90 km/h och med vajermitträcke, omväxlande med mitträfflor.<br><br>

            1980 bestämdes att Riksväg 15 skulle bli Europaväg under beteckningen E66. Kristianstad, Karlskrona och Kalmar ville få en Europaväg vid sina städer i hopp om att kommunikationerna skulle förbättras med en uppklassificerad väg.<br><br>

            1992 uppdaterade Vägverket de svenska vägnumren i enlighet med det reviderade Europavägnätet. E66 blev nuvarande E22 på den 560 km långa sträckan mellan Trelleborg (mot tidigare Limhamn) och Norrköping.<br><br>

            E22 är en väst-östlig Europaväg, som går mellan Holyhead i Storbritannien och Isjim i Ryssland, via Holland, Tyskland, Sverige och Lettland. Sträckan är totalt 5.320 km.<br><br>

            Utbyggnaden av E22 har gått långsamt vilket hämmat utvecklingen i sydöstra Sverige. I Blekinge är E22 en blandning av motorväg, 2+1-väg, vanlig tvåfilig väg, rondeller och till och med stoppljus(!). Hastighetsgränserna varierar från 30 till 110 km/h.<br><br>

            Motorväg planeras från Sölvesborg, där nuvarande motorvägen slutar, till Karlskrona och österut mot Jämjö. Huruvida detta kommer att byggas och när är omtvistat.<br><br>

            Den första motorvägen i Sveriges byggdes i betong mellan Malmö och Lund och öppnades 1953. Motorväg kallades i början för autostrada eftersom Italien var först med det redan på 1920-talet.<br><br>

            Den viktiga väst-östliga kustlandsvägen genom Blekinge har genom årtusendena haft följande namn: Landsvägen, Kalmarvägen, Via Regia - ”Kungsvägen”, 1944: Riksväg 4 - ”Riksfyran”, 1962: Riksväg 15, 1980: E66, 1992: E22.<br><br>

            Bredden har gått från stigens 1 meter till motorvägens 24,5 meter och farten från 5 till 110 km/h.<br><br>

            På Europaväg 22 får bilar, motorcyklar, bussar och lastbilar köra – men inte gående, cyklande, ridande, mopeder, traktorer eller motorredskap.<br><br>

