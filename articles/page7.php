        <h1>Nättraby Vägmuseum invigt 27 juli 2014</h1>
        <p>Nättraby Vägmuseum, vid E22 väster om Karlskrona, har officiellt invigts genom att en stor informationsskylt satts upp på rastplatsen vid den gamla stenbron, plus att broschyrer tryckts upp.<br><br>

        Invigningen firades med 100-talet besökande, en parad av resande for över stenbron; vandraren, cyklisten, motorcyklisten och slutligen bilen med förare, bandklippning, en tre meter lång tårta i form av en asfalterad raksträcka, vägrelaterade låtar från bandet On The Road samt en avslutande guidning till de fyra närmaste vägmiljöerna.<br><br>

        Nättraby Vägmuseum är världens första friluftsmuseum för vägarnas historia på land, räls, vatten och is. Museet består av 14 befintliga vägmiljöer i Nättraby socken. Från 1000-åriga stigen och 1600-talets stenmurskantade Via Regia till 1900-talets gatstensbilagda riksväg och E22 motorväg. Därtill kommer järnvägar, cykelvägen, vattenvägen Nättrabyån, isvägen, milstolpar, namnminnet Ryttarliden och tvåvalviga stenbron.<br><br>

        Sedan 2009 har information om Nättraby Vägmuseum publicerats på http://www.nattrabyvagmuseum.se men nu finns också fysisk skylt och broschyrer.<br><br>

