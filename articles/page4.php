        <h1>Nättraby Vägmuseum - om tillkomste</h1>
        <p>Världens första friluftsmuseum för vägarnas historia på land, räls, vatten och is.</p>
        <p>Nättraby Hembygdsförening i Blekinge har tillsammans med intressenter skapat Nättraby Vägmuseum – världens första friluftsmuseum för vägarnas historia på land, räls, vatten och is.<br><br>

        I Nättraby väster om Karlskrona finns nämligen en unik koncentration av gamla och nya landvägar, järnvägar och segelbara Nättrabyån.<br><br>

        Redan 1995 kläckte journalisten Peter Öjerskog från Karlskrona idén om ett vägmuseum i det fria i Nättraby. Förslaget mottogs positivt men det omfattande arbetet med att göra Karlskrona till marint världsarv kom emellan.<br><br>

        2006 kontaktade Nättraby Hembygdsförening med ordförande Ingegerd Holm i spetsen återigen Peter Öjerskog. En styrgrupp och en referensgrupp bildades för Nättraby Vägmuseum. Huvudintressenter är Nättraby Hembygdsförening, Karlskrona kommun, Vägverket, länsstyrelsen i Blekinge och Blekinge Museum.<br><br>

        Nättraby Vägmuseum är ett friluftsmuseum där befintliga vägmiljöer används. Den informella rastplatsen vid E22 avfart 61 till Nättraby används som centralplats för vägmuseet.<br><br>

        Nättraby Vägmuseum består av 14 utvalda vägmiljöer i Nättraby socken. Vägmiljöerna är:</p>
        <p> 01- Hålvägen – stigen <br>
        02 Via Regia – landsvägen <br>
        03 Värendsvägen – handelsvägen <br>
        04 Skillinge – övergivna vägen <br>
        05 Milstolparna – vägmärkena <br>
        06 Ryttarliden – namnminnet <br>
        07 Riks 4 – gatstensvägen <br>
        08 E22 – motorvägen <br>
        09 Cykelvägen – bilfria vägen <br>
        10 Kustbanan – järnvägen <br>
        11 Krösnabanan – smalspåret <br>
        12 Nättrabyån – vattenvägen <br>
        13 Isvägen – vintervägen <br>
        14 Stenbron – vägen över vatten</p>
        <p>Även om Blekinge alltid betraktats som ett randområde av först Danmark och från 1658 Sverige så låg landskapet välbekant mitt vid sjövägen mellan danernas och svearnas centralområden. Kustlandsvägen genom Blekinge har också alltid varit viktig för handel, kyrkan och militären. <br><br>

        Stigar och vägar, vattenvägar och vintervägar har funnits lika länge som människan. Ända fram till 1500-talet fanns egentligen bara stigar på land i Skandinavien. Först Christian IV och sedan Karl XI beordrade att Blekinges kuststig skulle breddas till vagnväg.<br><br>

        Ända fram till järnvägens revolution under andra halvan av 1800-talet var det faktiskt sjövägarna och vintervägarna som dominerade. Med bilens och motorcykelns intåg på 1900-talet utvecklades landvägarna mer än under hela sin tidigare historia.<br><br>

        Information om Nättraby Vägmuseum finns än så länge enbart digitalt på sajten http://www.nattrabyvagmuseum.se. Där finns också GPS-koordinater angivna till de 14 utvalda vägmiljöerna. Men arbete pågår nu med att också skapa ett fysiskt vägmuseum med informationsskyltar på plats.<br><br>

        Nättraby Vägmuseum förväntas locka en allmän publik, såväl svensk som utländsk. Genom sitt läge precis vid Europaväg 22 är museet extremt tillgängligt, framför allt för bilister, som utgör den största trafikgruppen. Men även gående, cyklister, bussresenärer och (på promenadavstånd) båtpassagerare kan nå museet. Tåget stannar inte längre vid Nättraby norra.<br><br>

        Vägverket Region Sydöst har bekostat en nytänkande förslagskiss på paviljonger, skyltar och parkering för Nättraby Vägmuseum som utarbetats av landskapsarkitekt Peter Bergholm, och ska förhoppningsvis bli verklighet i framtiden.<br><br>

        Infoskyltar sätts upp vid de 14 utvalda vägmiljöerna. Fyra informationspaviljonger byggs på den nu informella rastplatsen vid stenbron, som stängs av för fordon. En parkeringsplats byggs öster om Ekbergska huset, som ägs av Nättraby Hembygdsförening. <br><br>

        Besökarna går via en nyanlagd gångväg längs Gamla Landsvägen förbi Ekbergska huset och över den magnifika stenvalvsbron över Nättrabyån för att nå paviljongerna. En perfekt entré för fysiska Nättraby Vägmuseum!<br><br>

