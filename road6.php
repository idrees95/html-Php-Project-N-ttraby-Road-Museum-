        <h2>Ryttarliden – namnminnet</h2>
        <p>
            Det intressantaste med Ryttarliden (lid = backe) är att den gamla benämningen från tiden då man färdades till häst lever kvar än idag. För i tiden hade nämligen alla rejäla backar ett namn hos lokalbefolkningen! Namnet finns nedskrivet 1827 (Ryttareliden) och används fortfarande i folkmun i trakten, men existerar idag officiellt endast som busshållplatsen Ryttarliden i östra änden, där den gamla landsvägen viker 90 grader norrut. Faktum är att den asfalterade vägen officiellt har lokala namnet Gamla Landsvägen, men också är länsväg 679.<br><br>

            De långa och branta backarna över bergshöjden öster om Nättraby har varit ett gissel för vandrare, ryttare och vagnekipage i alla tider. Namnet Ryttarliden är bara ett exempel på blandningen av gammalt och nytt i denna vägmiljö.

            Snett åt nordväst syns en rest av 1640-talets Via Regia, kungsvägen som breddats för vagnar. Förmodligen var det just den ursprungliga 2500-åriga kuststigen som breddades eftersom inga andra spår finns av den i området. Det historiska avsnittet har grusats av nybyggda Skärfva by för att markeras och fungera som gångväg.<br><br>

            Tyvärr avbryts Via Regia efter en kort bit av moderna hus men grusade Via Regia fortsätter ringla ytterligare hundratalet meter västerut, och ännu ett stycke som väg med hjulspår innan den försvinner ut på dagens asfalterade väg. Via Regia är igenväxt men synlig österut där den går söder om Gamla Landsvägen.<br><br>

            Kustlandsvägen vid Ryttareliden byggdes om 1857 för att minska backigheten. Entreprenören A. G. Fagerlund beviljades statsanslag på 3.766 riksdaler och 30 skilling banco för jobbet.<br><br>

            En rakare landsväg sprängdes ner i berget 1934 för att ytterligare minska backens lutning. Från början belagd med smågatsten men från 1950-1960-talen asfalterad. 2004 rev Vägverket upp gatstenen, som legat perfekt konserverad under asfalten, och ny asfalt lades på. Samtidigt försvann två unika gatstensbelagda rastplatser/parkerings-fickor på vardera sidan av vägen, precis bakom krönet västerut. P-fickan på södra sidan skymtar kvar men har nu bara grusbeläggning.<br><br>

            I samband med byggande av Skärfva by i början av 2000-talet skapades flera moderna trafiksäkerhetsåtgärder på Ryttarliden. En separat gång- och cykelbanan drogs parallellt norr om vägen från Nättraby. Tyvärr upphör gång/cykelbanan tillfälligt österut eftersom det var för dyrt att spränga bort mer berg för att bredda vägen. Men sedan fortsätter en helt separat bana mellan E22 och järnvägen ända in till Karlskrona.<br><br>

            För att nå busshållsplatsen på södra sidan av vägen har två över-gångsställen byggts över Gamla Landsvägen vid Skärfva by. För att öka trafiksäkerheten har hastigheten sänkts dramatiskt till 30 km i timmen. Övergångsställena har markerats med vita stolpar som är försedda med reflexer. Som en sista åtgärd har farthinder i form av två vägbulor i betong anlagts i asfalten för att tvinga ner farten. För första gången i historien har alltså vägbyggaren medvetet försämrat vägens framkomlighet!<br><br>

            Busshållplatsen Skärfva by har på norra sidan fått en rejäl bussficka, lätt och säkert åtkomlig från gångbanan. Lika bra är inte hållplatsen på södra sidan av utrymmesskäl.<br><br>
