        <h2>Cykelvägen – bilfria vägen</h2>
        <p>
            Cykelvägen mellan Nättraby och Skärva är den nyaste vägen i Nättraby Vägmuseum. Den stod klar våren 2007 efter ett halvårs byggande. Ett bevis på att det byggs helt nydragna vägar än idag.<br><br>

            Cykelvägen är skyltat som cykelväg, men egentligen är det en Gång- och Cykelväg. Cykelväg innebär också att mopeder klass 2 får köra där. På fackspråk är vägen en GCM-väg: Gång-Cykel-Moped-väg.<br><br>

            Cykelvägen är cirka 2 kilometer lång och 2,5-3 meter bred och asfalterad. Den budgeterades till 2,5 miljoner kronor men blev något dyrare på grund av dåvarande högkonjunkturen.<br><br>

            Cykelvägen går mellan östliga villaområdet i Nättraby/allévägen till Stora Vörta och österut till vägen till Skärva.<br><br>

            Det är Vägverket som finansierat cykelvägen medan Karlskrona kommun svarat för genomförandet. Cykelvägen är nämligen att betrakta som en parallellväg till E22, där cyklar förstås är förbjudna på motorvägen. Därför har Vägverket erbjudit ett cykelalternativ.<br><br>

            Intressant är att cykelvägen utnyttjat de förutsättningar som funnits i terrängen och den ligger därför naturligt i landskapet utan att upplevas som ett intrång.<br><br>

            Första sträckan från Nättraby och österut går spikrakt över åkermark parallellt med en befintlig stenmur. I slänten upp mot skogshöjden utnyttjas den gamla järnvägsvallen som blev kvar när kustbanan rätades ut. Därefter går cykelvägen dramatiskt vackert via banvallens gamla sprängning genom berget.<br><br>

            Vidare österut går cykelvägen nerför skogshöjden på en gammal traktorväg. Avslutningsvis går asfalterade cykelvägen på en äldre smal grusväg parallellt nedanför nuvarande banvallen för att slutligen möta vägen mot Skärva.<br><br>

            Längs cykelvägen över åkermarken finns en parallell inhägnad som gör att markägarens djur hålls utanför. I slänten vid gamla järnvägsvallen finns ett metallräcke som skyddar för branten.<br><br>

            Cykelvägen har också gjort berghällen mitt ute på åkern tillgänglig. På granithällen finns nämligen mångtusenåriga skålgropar och ett utmejslat fotavtryck som kan minna om gamla tiders färdsätt.<br><br>

            Anledningen till att nya cykelvägen byggdes berodde dels på ett tryck från invånarna i Nättraby, dels politikernas värnande om miljö, hälsa och trafiksäkerhet. Men största anledningen var att tidigare enda cykelvägen gick längs länsväg 679 över branta och i alla tider fruktade Ryttarliden en bit nordöst om Nättraby. I genomsprängningen där finns heller inte utrymme för separat cykelväg utan cyklande och gående tvingas ut i trafiken.<br><br>

            Genom att nya cykelvägen kunde utnyttja gamla järnvägsdragningens genomsprängning genom berget blev den mindre kuperad och därmed betydligt attraktivare för cyklisterna!<br><br>

            Separat cykelväg bör höja trafiksäkerheten och kännas tryggare. Cyklister, gående och mopedister slipper bilar, bussar och lastbilar – och vice versa.<br><br>

            Vid Skärva möter den nya cykelvägen 1980-talets cykelväg mellan Skärva och Rosenholm. Vidare fortsätter cykelvägen ända in till Karlskronas centrum Trossö. Det går numera att cykla hela sträckan Nättraby-Karlskrona på separat cykelväg.<br><br>

            Trots att nya cykelvägen byggdes på mindre än ett halvår tog den hela 14 år att planera. Detta på grund av överklagade detaljplaner och att uppgörelse med markägare tog tid.<br><br>

            Från början räckte inte budgeten till för belysning. Ändå drogs kabel och stolpfundament smart nog när man ändå höll på och schaktade. Nyfunna skräcken för koppartjuvar gjorde att man sedan satte upp lyktstolpar för att dölja den dyrbara metallen. Till slut insåg man att det var lika bra att sätta in belysning. Kommunen sköt till pengar för stolparna, Vägverket för armaturerna.

            På cykelvägen får man gå, cykla och köra moped av klass 2, nya mopeder som går max 25 kilometer i timmen och gamla mopeder som går 30 kilometer i timmen. Cykelvägen är alltså inte helt befriad från motorfordon och buller. Däremot får man inte köra de nya EU-mopederna som går 45 kilometer i timmen.<br><br>

            Trots att cykeln ursprungligen uppfanns före motorcykeln och bilen var det först i slutet av 1900-talet som man började bygga särskilda cykelvägar i Sverige.<br><br>

            I slutet av 1700-talet kom de första ”cyklarna”. Byggda i trä saknade de både styrning och pedaler, kallades ”hobby horses” och hade ofta ett snidat häst- eller drakhuvud som frontprydnad.<br><br>

            1- 1817 uppfanns ”löphjulingen” som fick styrning.<br><br>

            2- 1868 kom pedaler på framhjulet, framhjul som blev allt högre för att få lämplig direktutväxling, så kallade ”höghjulingar”.<br><br>

            3- 1876 lanserades tre- och fyrhjuliga cyklar.<br><br>

            4- 1884 blev höghjulingarna lägre tack vare utväxling i framhjulet.<br><br>

            5- 1884 kom kedjedrivna ”säkerhetscyklar” med korsram och gummiringar.<br><br>

            6- 1890 uppfanns luftfyllda däcket. Sedan dess har cyklarna haft samma grundkonstruktion ända in i våra dagar.<br><br>

            Cykeln är ett lysande transportmedel. Vid cykling på slät mark förbrukar man bara en femtedel av den energi som behövs för att gå samma sträcka, samtidigt som cykeln är avsevärt snabbare än en människa till fots.<br><br>

            I början i Sverige måste cykeln ha registreringsskylt där hemstaden angavs med en bokstavskombination.<br><br>

            På 1930-talet fanns det två miljoner cyklar i landet, en på var tredje invånare.<br><br>
